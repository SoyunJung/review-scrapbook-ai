def clear_existing_demo_records() -> None:
    with sqlite3.connect(DB_PATH) as connection:
        connection.row_factory = sqlite3.Row
        record_rows = connection.execute(
            "SELECT id FROM scrapbook_records WHERE provider = 'demo'"
        ).fetchall()
        record_ids = [int(row["id"]) for row in record_rows]
        if record_ids:
            placeholders = ",".join("?" for _ in record_ids)
            connection.execute(f"DELETE FROM record_assets WHERE record_id IN ({placeholders})", record_ids)
            connection.execute(f"DELETE FROM record_embeddings WHERE record_id IN ({placeholders})", record_ids)
            connection.execute(f"DELETE FROM scrapbook_records WHERE id IN ({placeholders})", record_ids)
        connection.execute(
            """
            DELETE FROM scrapbook_works
            WHERE id NOT IN (
                SELECT DISTINCT work_id
                FROM scrapbook_records
                WHERE work_id IS NOT NULL
            )
            """
        )
        connection.commit()


def build_answers(sample: dict[str, Any]) -> tuple[list[str], dict[str, str]]:
    questions = list(DEFAULT_QUESTIONS)
    answers = {
        question: answer
        for question, answer in zip(DEFAULT_QUESTIONS, sample["answers"], strict=False)
    }
    for question, answer in sample.get("followups", {}).items():
        questions.append(question)
        answers[question] = answer
    if sample.get("free_text"):
        answers["자유 텍스트 단상"] = sample["free_text"]
    return questions, answers


def main() -> None:
    init_db()
    clear_existing_demo_records()

    created: list[tuple[int, str]] = []
    for sample in SAMPLES:
        reflection_input = ReflectionInput(
            content_type=sample["content_type"],
            title=sample["title"],
            creator=sample["creator"],
            appreciation_date=sample["appreciation_date"],
            status=sample["status"],
            work_context=sample["work_context"],
            spoiler_scope=sample["spoiler_scope"],
            initial_note=sample["answers"][0],
            free_text=sample["free_text"],
        )
        questions, answers = build_answers(sample)
        record_id = save_record(
            reflection_input=reflection_input,
            questions=questions,
            answers=answers,
            memo=sample["memo"],
            edited_memo=sample["memo"],
            provider="demo",
            force_new_work=True,
            input_mode=sample["input_mode"],
            appreciation_stage=sample["appreciation_stage"],
        )
        created.append((record_id, sample["title"]))

    print(f"created={len(created)}")
    for record_id, title in created:
        print(f"{record_id}: {title}")
    print(f"total={len(list_records())}")


if __name__ == "__main__":
    main()
